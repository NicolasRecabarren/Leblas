function loadingBtn($texto=null, $iconClass=null, $linkCake=false){
	$texto=(is_null($texto))?"Procesando...":$texto;
	$iconClass=(is_null($iconClass))?"	fa fa-refresh fa-spin":$iconClass;

	if(!$linkCake){
		$html ="data-loading-text=\"<i class='$iconClass'></i> $texto\"";
	}else{
		$html = array("data-loading-text" => "<i class='$iconClass'></i> ".$texto);
	}
	return $html;
}

function dbug($array){
	$bt = debug_backtrace();
	$caller = array_shift($bt);
	echo $caller['file']." ".$caller['line'];
	echo "\n";
	echo "<pre>";
	print_r($array);
	echo "</pre>";
}

